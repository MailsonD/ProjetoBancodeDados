CREATE TABLE Funcionario(
	Matricula VARCHAR(10) NOT NULL UNIQUE,
	CPF CHARACTER(10) NOT NULL,
	Nome VARCHAR(100) NOT NULL,
	Telefone INT NOT NULL,
	Cidade VARCHAR(50) NOT NULL,
	Bairro VARCHAR (20) NOT NULL,
	Rua VARCHAR(100) NOT NULL,
	Salario REAL NOT NULL,
	DataNascimento DATE NOT NULL,
	DataAdmissao DATE NOT NULL,
	RG VARCHAR(15) NOT NULL,
	Setor VARCHAR(20) NOT NULL,
	Supervisor VARCHAR(10),
	CONSTRAINT FK_Super FOREIGN KEY (Supervisor) REFERENCES Funcionario(Matricula),
	CONSTRAINT PK_FUNC PRIMARY KEY (Matricula),
	CONSTRAINT K_RG UNIQUE (RG),
	CONSTRAINT K_CPF UNIQUE (CPF),
	CONSTRAINT SalarioMinimo CHECK (Salario>940)
);